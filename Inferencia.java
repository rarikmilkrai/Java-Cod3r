package fundamentos;

public class Inferencia {
	public static void main(String[] args) {
		
		double a = 4.5;
		System.out.println(a);
	//Inferência é quando o programa diz o tipo de variavel,
	//proxima linha tem uma variavel que nao tem definição: double,int...
		var b = 4.5;
		System.out.println(b);
		
		var c = "Texto";
		System.out.println(c);
		
	}
}
